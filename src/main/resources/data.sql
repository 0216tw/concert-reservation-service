-- RESERVATION 데이터 삽입
INSERT INTO reservation (concert_id, concert_dy, seat_no, seat_grade, price, temp_reserve_user_id, temp_reserve_expired_at)
SELECT
    concert_id,
    concert_dy,
    seat_no,
    seat_grade,
    price,
    '0000-0000-0000-0000' AS temp_reserve_user_id, -- 임의의 사용자 ID 생성 (1~10)
    DATEADD(MINUTE , 5 , CURRENT_TIMESTAMP) AS temp_reserve_expired_at
FROM
    concert_seat
WHERE
    ( seat_grade = 'VVIP' OR state = 'ING' ) and concert_id != 3;

